function span_breadCrumb() {
	breadCrumb = $(".breadCrumb").html().split('/');
	breadCrumbLength = breadCrumb.length;
	breadCrumbspan = '<span>' + breadCrumb[0];
	if (breadCrumbLength >= 2) {
		for (i = 1; i < breadCrumbLength; i++) breadCrumbspan += '</span>/<span>' + breadCrumb[i];
	}
	breadCrumbspan += '</span>';
	$(".breadCrumb").html(breadCrumbspan);
	
	$(".breadCrumb span").bind("click touchstart", function(){
		var nom = $(this).index();
		breadCrumb = window.breadCrumbValues.split('/');
		if (nom < (breadCrumb.length - 1)) {
		
			if ($(".successDiv").css('display') == 'block') {
				$(".drag_drop").fadeIn(300);
				$(".successDiv").hide();
			}
			
			if($(".left-third-image").css("display") == 'block') {
				$(".left-image").css("margin-top","184px");
	        }
			
			window.breadCrumbValues = breadCrumb[0];
			for (i = 1; i <= nom; i++) window.breadCrumbValues += '/' + breadCrumb[i];
			getImages(breadCrumb[nom]);
		}
	});
}

