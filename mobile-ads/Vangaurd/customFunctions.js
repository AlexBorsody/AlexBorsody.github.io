function lastStep()
{
    var steps = window.breadCrumbValues.split('/');
    function toObject(arr) {
        var rv = {};
        for (var i = 0; i < arr.length; ++i)
        rv[i] = arr[i];
        return rv;
    }

    var stepsObject = toObject(steps);
    RAD.analytics({e:"circlecomplete", v:stepsObject });
}

